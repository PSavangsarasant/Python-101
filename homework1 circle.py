import turtle
t=turtle.Pen()
t.shape('turtle')
t.clear()
for i in range(10):
    t.goto(0,0)
    t.pencolor('red')
    t.penup()
    t.goto(i*3,i*3)
    t.pendown()
    t.circle(i*3)
    t.penup()
    t.goto(0,0)
    t.pencolor('orange')
    t.penup()
    t.goto(-i*3,i*3)
    t.pendown()
    t.circle(i*3)
    t.penup()
    t.goto(0,0)
    t.pencolor('yellow')
    t.penup()
    t.goto(i*3,-i*3)
    t.pendown()
    t.circle(-i*3)
    t.penup()
    t.goto(0,0)
    t.pencolor('blue')
    t.penup()
    t.goto(-i*3,-i*3)
    t.pendown()
    t.circle(-i*3)
    t.penup()
t.goto(0,0)
